class VariablesandDataTypes {
    public static void main(String[] args) {
        // Declare and initialize variables
        int age = 25; // Integer variable
        double height = 5.9; // Double variable
        String name = "SUNNY"; // String variable
        boolean isStudent = true; // Boolean variable

        // Print the variables
        System.out.println("Age: " + age + ", Height: " + height + ", Name: " + name + ", Is Student: " + isStudent);
        
    }
}